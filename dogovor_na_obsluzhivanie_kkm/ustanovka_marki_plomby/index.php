<p><font size="2" face="Arial">4.5. При проведении технического обслуживания ККТ, проверке исправности ККТ, ремонте и замене накопителей фискальной памяти ККТ специалист ЦТО удаляет вторые - пятые части марки-пломбы с ККТ, а по окончании технических процедур устанавливает новые.</font></p>

<p><font size="2" face="Arial">В паспорте и учетном талоне ККТ специалист ЦТО делает записи о причине замены марки-пломбы, наклеивает первую часть марки-пломбы в учетный талон ККТ, а шестую часть марки-пломбы в паспорт ККТ.</font></p>

<p><font size="2" face="Arial">Если ЦТО осуществляет техническую поддержку ККТ, которая ранее находилась на техническом обслуживании в другой организации, или уже обеспечивает техническую поддержку тех моделей ККТ, которые не были укомплектованы ни паспортами, ни учетными талонами ККТ, она должна самостоятельно приобретать учетные талоны ККТ.</font></p>

<p><font size="2" face="Arial">4.6. В случае повреждения (нарушения целостности) вторых - пятых частей марки-пломбы пользователь ККТ прекращает эксплуатацию этой ККТ и сообщает об этом в обслуживающую организацию (ЦТО).</font></p>

<p><font size="2" face="Arial">Эксплуатация ККТ возобновляется после проверки специалистом ЦТО исправности этой ККТ, отсутствия несанкционированных поставщиком ККТ доработок аппаратного и программного обеспечения и установки новой марки-пломбы согласно требованиям пункта 4.2 настоящего Порядка.</font></p>

<p><font size="2" face="Arial">4.7. При установке марки-пломбы на ККТ на второй - пятой частях марки-пломбы, наклеиваемых на ККТ, на специальном поле ставятся подпись специалиста ЦТО, установившего марку-пломбу, и дата ее установки.</font></p>

<p><font size="2" face="Arial">Если используются не все элементы второй - пятой частей марки-пломбы, то они устанавливаются на ККТ в порядке нарастания их номеров. То есть нельзя, например, установить на ККТ элементы под номерами 1, 2 и 4, не использовав элемент под номером 3. Или установить элементы под номерами 2 и 3, не использовав элемент под номером 1.</font></p>

<p><font size="2" face="Arial">4.8. Снятие и установка марок-пломб специалистами ЦТО (в том числе поврежденной марки-пломбы) происходит в присутствии сотрудника налогового органа, за исключением случая окончания срока действия пломбы.</font></p>

<p><font size="2" face="Arial">4.9. При отправке ККТ в ремонт специалист ЦТО заносит сведения о марках-пломбах, установленных на ККТ, в акт о снятии показаний контрольных и суммирующих денежных счетчиков при сдаче (отправке) контрольно-кассовой техники в ремонт и при возвращении ее в организацию (форма N КМ-2), а после окончания ремонта устанавливает на ККТ новые марки-пломбы.</font></p>

<p><font size="2" face="Arial">4.10. ЦТО в договоре на техническое обслуживание и ремонт ККТ в качестве оснований для расторжения договора предусматривает факт повреждений марок-пломб более 2 раз или их отсутствия на ККТ, на которой они были установлены.</font></p>

<p><font size="2" face="Arial">4.11. При расторжении договора с пользователей ККТ специалист ЦТО в присутствии сотрудника налогового органа удаляет все вторые - пятые части марки-пломбы.</font></p>

<p><font size="2" face="Arial">4.12. ЦТО в течение двух дней информирует уполномоченную организацию о расторжении договора на техническое обслуживание и ремонт ККТ.</font></p>

<p><font size="2" face="Arial">4.13. По окончании срока действия марок-пломб специалист ЦТО заменяет их на новые.</font></p>

<p><font size="2" face="Arial">4.14. ККТ должна быть опломбирована специалистами ЦТО соответствующими марками-пломбами в сроки, указанные в пункте 1.8 настоящего Порядка.</font></p>

<p><font size="2" face="Arial">4.15. ЦТО, допустивший к эксплуатации ККТ, опломбированную согласно требованиям настоящего Порядка, но не соответствующую требованиям нормативно-технической документации на эту ККТ, при условии подлинности и целостности установленных вторых - пятых частей марки-пломбы, несет ответственность в соответствии с действующим законодательством РФ.</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">5. Нанесение контрольно-учетной информации на марки-пломбы</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">5.1. Перед выдачей марок-пломб ЦТО на них наносится контрольно-учетная информация.</font></p>

<p><font size="2" face="Arial">5.2. Нанесение всей контрольно-учетной информации на марку-пломбу производится в уполномоченной организации.</font></p>

<p><font size="2" face="Arial">5.3. На все шесть частей марки-пломбы должны наноситься индивидуальные серия и номер, региональный код Московской области &quot;50&quot; и дата изготовления (срок действия) марки-пломбы (квартал и год).</font></p>

<p><font size="2" face="Arial">Региональный код Московской области &quot;50&quot; наносится слева от надписи &quot;РОССИЯ&quot;, расположенной в левой части марки-пломбы.</font></p>

<p><font size="2" face="Arial">Индивидуальные серия и номер марки-пломбы наносятся под надписью &quot;РОССИЯ&quot;, расположенной на марке-пломбе.</font></p>

<p><font size="2" face="Arial">Дата изготовления (срок действия) марки-пломбы наносится в правой части марки-пломбы под надписью &quot;ДАТА ИЗГОТОВЛЕНИЯ&quot;.</font></p>

<p><font size="2" face="Arial">5.4. На первую и шестую части марки-пломбы помимо индивидуальных серии и номера, регионального кода Московской области и даты изготовления (срока действия) марки-пломбы наносится дополнительная контрольно-учетная информация.</font></p>

<p><font size="2" face="Arial">На первую часть марки-пломбы (учетный талон) наносится линейный штриховой код, содержащий индивидуальный номер марки-пломбы.</font></p>

<p><font size="2" face="Arial">На шестую часть марки-пломбы (паспорт) наносится буквенно-цифровая информация в виде наименования ЦТО, которому выдаются марки-пломбы и двумерный штриховой код.</font></p>

<p><font size="2" face="Arial">Двумерный штриховой код содержит в себе следующую информацию:</font></p>

<p><font size="2" face="Arial">- код ЦТО;</font></p>

<p><font size="2" face="Arial">- период действия марки-пломбы (квартал и год);</font></p>

<p><font size="2" face="Arial">- номер заявки на изготовление марки-пломбы;</font></p>

<p><font size="2" face="Arial">- номер марки-пломбы в заявке;</font></p>

<p><font size="2" face="Arial">- дата нанесения контрольно-учетной информации.</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">6. Учет и отчетность при использовании марок-пломб</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">6.1. В случае утери (хищения, порчи или иных обстоятельств) марок-пломб ЦТО в течение 3 дней сообщают в уполномоченную организацию о факте, количестве и учетных номерах марок-пломб с указанием причин.</font></p>

<p><font size="2" face="Arial">6.2. ЦТО на безвозмездной основе не реже одного раза в квартал обязан сообщать в уполномоченную организацию сведения об установленных марках-пломбах на ККТ, которые должны содержать следующую информацию:</font></p>

<p><font size="2" face="Arial">- наименование ЦТО с указанием ИНН и КПП;</font></p>

<p><font size="2" face="Arial">- наименование владельца ККТ с указанием ИНН, КПП;</font></p>

<p><font size="2" face="Arial">- адрес установки ККТ;</font></p>

<p><font size="2" face="Arial">- модель ККТ с указанием ее заводского номера, на которую установлены марки-пломбы, количество установленных марок-пломб на ККТ (шт.) и их учетные номера (марки-пломбы, установленные на данную ККТ).</font></p>

<p><font size="2" face="Arial">6.3. Все отчеты, передаваемые ЦТО в электронном виде в уполномоченную организацию, должны быть предварительно согласованы руководителем ЦТО.</font></p>


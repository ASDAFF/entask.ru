<p><font size="2" face="Arial">С учетом требований Федерального закона от 22.05.2003 N 54-ФЗ &quot;О применении контрольно-кассовой техники при осуществлении наличных денежных расчетов и (или) расчетов с использованием платежных карт&quot;, постановления Правительства Российской Федерации от 23.07.2007 N 470 &quot;Об утверждении Положения о регистрации и применении контрольно-кассовой техники, используемой организациями и индивидуальными предпринимателями&quot; и во исполнение постановления Правительства Московской области от 12.11.2004 N 684/45 &quot;О марках-пломбах, используемых на контрольно-кассовой технике в Московской области&quot;:</font></p>

<p><font size="2" face="Arial">1. Утвердить согласованный Управлением Федеральной налоговой службы по Московской области Порядок заказа, приобретения марок-пломб и опломбирования контрольно-кассовой техники на территории Московской области.</font></p>

<p><font size="2" face="Arial">2. Центрам технического обслуживания контрольно-кассовой техники Московской области принять к исполнению Порядок, утвержденный пунктом 1 настоящего распоряжения.</font></p>

<p><font size="2" face="Arial">3. Установить период, в течение которого будет осуществлен переход от марки-пломбы Московской области к маркам-пломбам образца, утвержденного приказом Министерства промышленности и энергетики Российской Федерации от 05.09.2007 N 351 &quot;Об утверждении образца марки-пломбы контрольно-кассовой техники&quot;, до 16 февраля 2008 года.</font></p>

<p><font size="2" face="Arial">4. Признать утратившим силу распоряжение Комитета потребительского рынка Московской области от 21.12.2004 N 6 &quot;О Порядке заказа, приобретения марок-пломб и опломбирования контрольно-кассовой техники на территории Московской области&quot;.</font></p>

<p><font size="2" face="Arial">5. Контроль за выполнением настоящего распоряжения оставляю за собой.</font></p>


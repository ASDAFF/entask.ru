<p align="center"><font size="2" face="Arial"><strong>ЗАКАЗА, ПРИОБРЕТЕНИЯ МАРОК-ПЛОМБ И ОПЛОМБИРОВАНИЯ</strong></font></p>

<p align="center"><font size="2" face="Arial"><strong>КОНТРОЛЬНО-КАССОВОЙ ТЕХНИКИ НА ТЕРРИТОРИИ</strong></font></p>

<p align="center"><strong><font size="2" face="Arial">МОСКОВСКОЙ ОБЛАСТИ</font></strong></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">Настоящий Порядок разработан с учетом требований Федерального закона от 22.05.2003 N 54-ФЗ &quot;О применении контрольно-кассовой техники при осуществлении наличных денежных расчетов и (или) расчетов с использованием платежных карт&quot;, постановления Правительства Российской Федерации от 23.07.2007 N 470 &quot;Об утверждении Положения о регистрации и применении контрольно-кассовой техники, используемой организациями и индивидуальными предпринимателями&quot;, приказа Министерства промышленности и энергетики Российской Федерации от 05.09.2007 N 351 &quot;Об утверждении образца марки-пломбы контрольно-кассовой техники&quot; и во исполнение постановления Правительства Московской области от 12.11.2004 N 684/45 &quot;О марках-пломбах, используемых на контрольно-кассовой технике в Московской области&quot;.</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">1. Общие положения</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">1.1. Настоящий Порядок устанавливает взаимоотношения по вопросу использования пломб на территории Московской области между центрами технического обслуживания, организацией - изготовителем пломб и организацией, осуществляющей приобретение пломб у изготовителя, их хранение, выдачу, а также ведение единого реестра учета движения пломб.</font></p>

<p><font size="2" face="Arial">1.2. Организацией - изготовителем пломб является победитель открытого конкурса, проводимого в установленном законодательством Московской области порядке среди организаций, осуществляющих деятельность по изготовлению защищенной от подделок полиграфической продукции (далее - организация-изготовитель).</font></p>

<p><font size="2" face="Arial">1.3. Организацией, осуществляющей приобретение пломб у организации-изготовителя, их хранение, выдачу центрам технического обслуживания ККТ, а также ведение единого реестра учета движения пломб, является победитель открытого конкурса, проводимого в установленном законодательством Московской области порядке (далее - уполномоченная организация).</font></p>

<p><font size="2" face="Arial">1.4. Пломба представляет собой марку-пломбу образца, утвержденного приказом Министерства промышленности и энергетики Российской Федерации от 05.09.2007 N 351 &quot;Об утверждении образца марки-пломбы контрольно-кассовой техники&quot; (далее - ККТ).</font></p>

<p><font size="2" face="Arial">1.5. Марка-пломба состоит из шести частей, которые наклеиваются:</font></p>

<p><font size="2" face="Arial">- в учетный талон контрольно-кассовой техники (первая часть);</font></p>

<p><font size="2" face="Arial">- на контрольно-кассовую технику (вторая - пятая части);</font></p>

<p><font size="2" face="Arial">- в паспорт контрольно-кассовой техники (шестая часть).</font></p>

<p><font size="2" face="Arial">1.6. На марку-пломбу, наклеиваемую на ККТ, на специальном поле ставятся подпись специалиста ЦТО, установившего марку-пломбу, и дата ее установки. Подпись и дата ставятся шариковой ручкой синего цвета.</font></p>

<p><font size="2" face="Arial">1.7. Марки-пломбы используются для опломбирования всей ККТ, обслуживаемой ЦТО на территории Московской области.</font></p>

<p><font size="2" face="Arial">1.8. Срок действия марки-пломбы составляет один квартал, соответствует кварталу, указанному в поле даты изготовления марки-пломбы. Срок действия марки-пломбы продляется на время для ее замены. Замена марок-пломб осуществляется в течение 45 календарных дней следующего квартала. Отсчет 45 календарных дней начинается с 1 числа месяца наступившего квартала, а именно с 1 января, с 1 апреля, с 1 июля, с 1 октября.</font></p>

<p><font size="2" face="Arial">1.9. Продажа или передача другим лицам пломб, приобретенных ЦТО, запрещается.</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">2. Порядок заказа уполномоченной организацией марок-пломб</font></p>

<p align="center"><font size="2" face="Arial">и предоставления пломб организацией-изготовителем</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">2.1. Организация-изготовитель заключает с уполномоченной организацией договор, который должен быть согласован с Министерством потребительского рынка и услуг Московской области.</font></p>

<p><font size="2" face="Arial">2.2. Марки-пломбы изготавливаются на основании сводных заявок, представляемых уполномоченной организацией.</font></p>

<p><font size="2" face="Arial">2.3. Уполномоченная организация принимает от ЦТО заявки на изготовление марок-пломб не позднее 45 дней до начала квартала, составляет сводные заявки на изготовление необходимого количества марок-пломб, направляет их организации-изготовителю и контролирует выполнение заказа.</font></p>

<p><font size="2" face="Arial">2.4. Уполномоченная организация подает организации-изготовителю сводную заявку на изготовление марок-пломб не позднее 30 дней до начала квартала.</font></p>

<p><font size="2" face="Arial">2.5. В сводной заявке на изготовление марок-пломб должна содержаться информация о квартале, на который заказываются марки-пломбы, и количестве заказываемых марок-пломб.</font></p>

<p><font size="2" face="Arial">2.6. Организация-изготовитель представляет уполномоченной организации марки-пломбы не позднее 15 дней до наступления очередного квартала.</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">3. Порядок приобретения марок-пломб ЦТО</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">3.1. В целях выполнения настоящего Порядка уполномоченная организация заключает договор с ЦТО.</font></p>

<p><font size="2" face="Arial">3.2. Для изготовления марок-пломб ЦТО представляет не позднее 45 дней до начала квартала в уполномоченную организацию заявку, в которой указываются:</font></p>

<p><font size="2" face="Arial">- полное и сокращенное наименование ЦТО;</font></p>

<p><font size="2" face="Arial">- место нахождения ЦТО;</font></p>

<p><font size="2" face="Arial">- идентификационный номер налогоплательщика;</font></p>

<p><font size="2" face="Arial">- квартал, на который заказываются марки-пломбы;</font></p>

<p><font size="2" face="Arial">- количество заказанных марок-пломб (шт.).</font></p>

<p><font size="2" face="Arial">Заявка должна быть подписана руководителем и главным бухгалтером ЦТО и заверена печатью. Исправления в заявке не допускаются.</font></p>

<p><font size="2" face="Arial">К заявке прилагается копия платежного поручения, подтверждающего внесение платежа за марки-пломбы с учетом заказываемого количества марок-пломб. В платежном поручении должно быть указано: &quot;Оплата за марки-пломбы для опломбирования ККТ&quot;.</font></p>

<p><font size="2" face="Arial">3.3. Для получения марок-пломб ЦТО представляет в уполномоченную организацию доверенность на право получения марок-пломб с указанием количества подлежащих выдаче марок-пломб, оформленную в установленном порядке.</font></p>

<p><font size="2" face="Arial">3.4. Марки-пломбы выдаются в соответствии с поданной ранее заявкой при условии своевременного представления ЦТО отчетности о движении марок-пломб по установленным формам.</font></p>

<p><font size="2" face="Arial">Основанием для отказа в выдаче марок-пломб является непредставление ЦТО документов, предусмотренных пп. 3.2-3.4 настоящего Порядка.</font></p>

<p><font size="2" face="Arial">3.5. После установки на ККТ марки-пломбы специалистом ЦТО ответственность за ее сохранность (целостность) несет пользователь ККТ. Это условие фиксируется в договоре на техническое обслуживание и ремонт ККТ между пользователем и ЦТО.</font></p>

<p><font size="2" face="Arial">3.6. В случае реорганизации, изменения наименования, местонахождения ЦТО он обязан в 5-дневный срок сообщить об этом в уполномоченную организацию.</font></p>

<p><font size="2" face="Arial">3.7. Сведения о выданных марках-пломбах отражаются в реестре учета движения марок-пломб, который ведется уполномоченной организацией. В реестре должна быть указана следующая информация:</font></p>

<p><font size="2" face="Arial">- наименование ЦТО с указанием ИНН и КПП;</font></p>

<p><font size="2" face="Arial">- количество выданных марок-пломб (шт.);</font></p>

<p><font size="2" face="Arial">- учетные номера выданных марок-пломб;</font></p>

<p><font size="2" face="Arial">- срок действия марок-пломб;</font></p>

<p><font size="2" face="Arial">- дата выдачи марок-пломб;</font></p>

<p><font size="2" face="Arial">- кому выданы марки-пломбы (Ф.И.О.);</font></p>

<p><font size="2" face="Arial">- кем выданы марки-пломбы (Ф.И.О.).</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p align="center"><font size="2" face="Arial">4. Порядок опломбирования контрольно-кассовой</font></p>

<p align="center"><font size="2" face="Arial">техники марками-пломбами</font></p>

<p><font size="2" face="Arial"> </font></p>
 
<p><font size="2" face="Arial">4.1. После проверки исправности, замены, ремонта или технического обслуживания контрольно-кассовой техники центром технического обслуживания на эту технику наклеивается марка-пломба, необратимо разрушающаяся или деформирующаяся при попытке ее удаления или при вскрытии или удалении корпуса контрольно-кассовой техники, а также окончание срока действия пломбы, при замене поврежденных пломб.</font></p>

<p><font size="2" face="Arial">4.2. Опломбирование ККТ марками-пломбами производится в соответствии с эксплуатационными документами на ККТ, в которых указаны места опломбирования, или рекомендациями к эксплуатационным документам, предоставленными поставщиками соответствующих моделей ККТ, а при их отсутствии:</font></p>

<p><font size="2" face="Arial">- в местах соединения частей корпуса ККТ;</font></p>

<p><font size="2" face="Arial">- на разъемах для подключения внешних устройств (при их наличии) следующим образом: если разъем не задействован в работе ККТ, она опломбируется на этом разъеме; если к разъемам ККТ подключены кабели, ККТ опломбируется в местах соединения кабеля с ККТ и с внешним устройством (если это технически осуществимо).</font></p>

<p><font size="2" face="Arial">4.3. При вводе ККТ в эксплуатацию специалист ЦТО устанавливает вторую - пятую части марки-пломбы на ККТ в соответствии с требованиями пункта 4.2 настоящего Порядка. Кроме этого, специалист ЦТО наклеивает первую часть марки-пломбы в учетный талон ККТ, а шестую часть марки-пломбы - в паспорт ККТ.</font></p>

